export @help, @stdout_to_pager, @out2pr

"""
    @help(f)

Open the documentation of the function, macro or other object `f` in pager.

# Examples

```julia-repl
julia> @help write
```
"""
macro help(f)
    # Get the string representation of `f`.
    # When calling @help for a macro, the resulting `Expr` contains a `LineNumberNode`
    # jumbling the resulting string, so use the `Symbol` instead.
    f_str = string(Meta.isexpr(f, :macrocall) ? f.args[1] : f)

    ex_out = quote
        # We do not need to verify if we are in an interactive environment because this mode is
        # only accessible through pager mode, which already checks it.
        try
            $TerminalPager.pager($TerminalPager._get_help($f_str); use_alternate_screen_buffer = true)
        catch err
            Base.display_error(stderr, err, Base.catch_backtrace())
        end
    end

    return esc(ex_out)
end

"""
    @stdout_to_pager(ex_in, pager_args...)

Capture the `stdout` generated by `ex_in` and show inside a pager.

`pager_args` are the keyword arguments of the `pager` function.

See also [`pager`](@ref).

!!! note
    The command **must** write to `stdout` explicitly. For example, `@stdout_to_pager 1`
    shows a blank screen since `1` does not write to `stdout`, but returns `1`.
    `@stdout_to_pager show(1)`, on the other hand, shows the number `1` inside the pager.

!!! note
    This macro can also be called using the shorter name `@out2pr` if no pager_args are
    needed.
"""
macro stdout_to_pager(ex_in, args...)
    all(x -> Meta.isexpr(x, :(=)), args) ||
        "All additional arguments to @stdout_to_pager must be keyword arguments." |>
        MethodError |> throw
    # Convert args from macro expressions to keyword arguments.
    kwargs = map(x -> Expr(:kw, x.args[1], x.args[2]), args)

    ex_out = quote
        hascolor     = get(stdout, :color, true)
        bypass_pager = get(stdout, :bypass_pager, false)
        old_stdout   = stdout
        buf          = IOBuffer()

        io = IOContext(
            buf,
            :bypass_pager => bypass_pager,
            :color        => hascolor,
            :limit        => false,
        )

        try
            Base.eval(:(stdout = $io))
            $(esc(ex_in))
            Base.eval(:(stdout = $old_stdout))
            pager(String(take!(buf)); $(kwargs...))
            close(io)
        finally
            Base.eval(:(stdout = $old_stdout))
        end

    end

    return ex_out
end

macro out2pr(ex)
    return :(@stdout_to_pager $(esc(ex)))
end

